# Write an R program to sort a Vector in ascending and descending order.

x <- c(10,30,20,40,75,60,100,95,110,102)
print("The value of x is :")
print(x)
print('x in ascending numbers :')
print(sort(x))
print('x in descending numbers :')
print(sort(x,decreasing=TRUE))
#Sort() function in R Language is used to sort a vector by its values. It takes
#Boolean value as argument to sort in ascending or descending order.
