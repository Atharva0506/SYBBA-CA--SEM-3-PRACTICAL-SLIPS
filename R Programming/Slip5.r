# Write an R program to find Sum, Mean and Product of a Vector.
x <- c(20, 40, 60)
print("Sum:")
print(sum(x))
print("Mean:")
print(mean(x))
print("Product:")
print(prod(x))

# The mean() function to calculate the mean. The mean is the average of a
data set.
# sum() function return the addition of the values passed as arguments to
the function.
# prod() function is used to return the multiplication results of all the
values present in its arguments.
